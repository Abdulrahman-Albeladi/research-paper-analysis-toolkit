def dataset_audit(df: pd.DataFrame) -> pd.DataFrame:
    observed = (
        df.groupby(["Language", "Label"], observed=True)
        .size()
        .reset_index(name="Observed_N")
    )
    full = pd.MultiIndex.from_product([LANGUAGES, LABELS], names=["Language", "Label"]).to_frame(index=False)
    audit = full.merge(observed, on=["Language", "Label"], how="left").fillna({"Observed_N": 0})
    audit["Observed_N"] = audit["Observed_N"].astype(int)
    audit["Expected_N"] = 50
    audit["Balanced_As_Designed"] = audit["Observed_N"].eq(audit["Expected_N"])
    return audit
