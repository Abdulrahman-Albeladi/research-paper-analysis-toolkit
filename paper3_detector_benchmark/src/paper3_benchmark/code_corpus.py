from __future__ import annotations

import csv
import hashlib
import os
import re
import shutil
import subprocess
import tempfile
from pathlib import Path
from urllib.parse import urlparse

CODE_EXTENSIONS = {
    ".py", ".js", ".ts", ".jsx", ".tsx", ".java", ".kt", ".kts", ".c", ".cc", ".cpp",
    ".cxx", ".h", ".hh", ".hpp", ".cs", ".go", ".rs", ".rb", ".php", ".swift", ".scala",
    ".sql", ".html", ".css", ".scss", ".m", ".r", ".jl", ".sh", ".vue", ".dart", ".lua", ".pl"
}
SKIP_DIRECTORIES = {
    ".git", ".github", ".idea", ".vscode", "__pycache__", "node_modules", "vendor", "dist",
    "build", "out", "coverage", ".next", ".gradle", ".pytest_cache", ".venv", "venv", "target",
    "bin", "obj", "assets", "images", "fonts"
}
SKIP_FILENAMES = {
    "package-lock.json", "yarn.lock", "pnpm-lock.yaml", "composer.lock", "poetry.lock", "Cargo.lock",
    "Gemfile.lock", "Pipfile.lock", "pubspec.lock"
}
BASE_SCORE = {
    ".py": 90, ".java": 88, ".kt": 88, ".c": 85, ".cc": 85, ".cpp": 85, ".cxx": 85,
    ".cs": 90, ".go": 90, ".rs": 92, ".rb": 80, ".php": 80, ".swift": 84, ".scala": 82,
    ".sql": 82, ".js": 78, ".ts": 82, ".jsx": 64, ".tsx": 68, ".vue": 70, ".dart": 78,
    ".m": 74, ".r": 74, ".jl": 74, ".sh": 56, ".html": 28, ".css": 14, ".scss": 18,
    ".lua": 72, ".pl": 70,
}


def parse_github_url(url: str) -> tuple[str, str, str | None] | None:
    parsed = urlparse(url.strip())
    if parsed.netloc.lower() not in {"github.com", "www.github.com"}:
        return None
    parts = [part for part in parsed.path.split("/") if part]
    if len(parts) < 2:
        return None
    owner, repo = parts[0], parts[1].removesuffix(".git")
    subpath = None
    if len(parts) >= 5 and parts[2] in {"tree", "blob"}:
        subpath = "/".join(parts[4:])
    return owner, repo, subpath


def _run(command: list[str], cwd: Path | None = None) -> tuple[bool, str]:
    result = subprocess.run(
        command,
        cwd=cwd,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        check=False,
    )
    return result.returncode == 0, result.stdout


def _is_binary(path: Path) -> bool:
    try:
        sample = path.read_bytes()[:8192]
    except OSError:
        return True
    return b"\x00" in sample


def _read_code(path: Path) -> str:
    for encoding in ("utf-8", "utf-8-sig", "cp1252", "latin-1"):
        try:
            return path.read_text(encoding=encoding)
        except UnicodeDecodeError:
            continue
    return path.read_text(encoding="utf-8", errors="ignore")


def _looks_minified(text: str) -> bool:
    lines = text.splitlines()
    if not lines:
        return True
    longest = max(map(len, lines))
    return longest > 5000 or (len(lines) < 5 and len(text) > 10_000)


def _generated_penalty(text: str) -> int:
    lowered = text.lower()
    markers = ("do not edit", "this file was generated", "autogenerated", "auto-generated", "@generated")
    return 60 if any(marker in lowered for marker in markers) else 0


def candidate_score(relative_path: str, text: str) -> int:
    extension = Path(relative_path).suffix.lower()
    lines = [line for line in text.splitlines() if line.strip()]
    if extension not in CODE_EXTENSIONS or len(lines) < 12 or len(lines) > 4000 or _looks_minified(text):
        return 0
    score = BASE_SCORE.get(extension, 40)
    lowered_path = relative_path.lower().replace("\\", "/")
    if re.search(r"(^|/)(src|app|server|backend|api|core|lib|internal|pkg|cmd|domain|logic|engine)(/|$)", lowered_path):
        score += 35
    if re.search(r"(^|/)(controllers?|services?|handlers?|routes?|middleware|auth|security)(/|$)", lowered_path):
        score += 45
    if re.search(r"(^|/)(test|tests|spec|fixtures|mocks|sample|examples?|docs?|assets?)(/|$)", lowered_path):
        score -= 35
    score += min(len(lines), 400) // 6
    comment_lines = sum(bool(re.match(r"^\s*(#|//|/\*|\*)", line)) for line in lines)
    score -= int((comment_lines / max(len(lines), 1)) * 40)
    score -= _generated_penalty(text)
    return max(score, 0)


def collect_candidates(repo_root: Path, base_path: Path, *, max_candidates: int = 60) -> list[dict[str, object]]:
    candidates: list[dict[str, object]] = []
    seen_hashes: set[str] = set()
    for root, directories, filenames in os.walk(base_path):
        directories[:] = [name for name in directories if name not in SKIP_DIRECTORIES]
        for filename in filenames:
            path = Path(root) / filename
            if path.name in SKIP_FILENAMES or path.suffix.lower() not in CODE_EXTENSIONS:
                continue
            if path.stat().st_size > 1_200_000 or _is_binary(path):
                continue
            text = _read_code(path)
            digest = hashlib.sha1(text[:250_000].encode("utf-8", errors="ignore")).hexdigest()
            if digest in seen_hashes:
                continue
            seen_hashes.add(digest)
            relative = str(path.relative_to(repo_root)).replace("\\", "/")
            score = candidate_score(relative, text)
            if score <= 0:
                continue
            candidates.append(
                {
                    "file_rel": relative,
                    "text": text,
                    "score": score,
                    "lines": len(text.splitlines()),
                    "words": len(text.split()),
                    "extension": path.suffix.lower(),
                }
            )
    return sorted(candidates, key=lambda row: (row["score"], row["lines"], row["words"]), reverse=True)[:max_candidates]


def select_candidates(
    candidates: list[dict[str, object]],
    *,
    target_lines: int = 800,
    target_words: int = 3000,
    max_files: int = 25,
) -> list[dict[str, object]]:
    selected: list[dict[str, object]] = []
    lines = words = 0
    for candidate in candidates:
        selected.append(candidate)
        lines += int(candidate["lines"])
        words += int(candidate["words"])
        if len(selected) >= max_files or lines >= target_lines or words >= target_words:
            break
    return selected


def prepare_code_corpus(url_csv: str | Path, output_dir: str | Path) -> None:
    output_dir = Path(output_dir)
    corpus_dir = output_dir / "trimmed_for_detection"
    reports_dir = output_dir / "candidate_reports"
    corpus_dir.mkdir(parents=True, exist_ok=True)
    reports_dir.mkdir(parents=True, exist_ok=True)
    urls = []
    with Path(url_csv).open(newline="", encoding="utf-8-sig") as handle:
        reader = csv.DictReader(handle)
        if "url" not in (reader.fieldnames or []):
            raise ValueError("URL CSV must contain a column named 'url'.")
        urls = [row["url"].strip() for row in reader if row.get("url", "").strip()]

    summaries: list[dict[str, object]] = []
    with tempfile.TemporaryDirectory(prefix="paper3_code_repos_") as temp:
        temp_root = Path(temp)
        for url in urls:
            parsed = parse_github_url(url)
            if not parsed:
                summaries.append({"url": url, "status": "invalid_url"})
                continue
            owner, repo, subpath = parsed
            slug = re.sub(r"[^A-Za-z0-9_.-]+", "_", f"{owner}_{repo}")
            repo_dir = temp_root / slug
            ok, log = _run(["git", "clone", "--depth", "1", f"https://github.com/{owner}/{repo}.git", str(repo_dir)])
            if not ok:
                summaries.append({"url": url, "status": "clone_failed", "message": log[-500:]})
                continue
            base_path = repo_dir / subpath if subpath and (repo_dir / subpath).exists() else repo_dir
            candidates = collect_candidates(repo_dir, base_path)
            selected = select_candidates(candidates)
            if not selected:
                summaries.append({"url": url, "status": "no_candidates"})
                continue
            output_file = corpus_dir / f"{slug}.txt"
            with output_file.open("w", encoding="utf-8") as handle:
                for index, item in enumerate(selected):
                    if index:
                        handle.write("\n############\n\n")
                    handle.write(str(item["file_rel"]).strip() + "\n")
                    handle.write(str(item["text"]).rstrip() + "\n")
            report = reports_dir / f"{slug}.csv"
            pd_rows = [{k: v for k, v in item.items() if k != "text"} for item in candidates]
            with report.open("w", newline="", encoding="utf-8-sig") as handle:
                writer = csv.DictWriter(handle, fieldnames=["file_rel", "score", "lines", "words", "extension"])
                writer.writeheader()
                writer.writerows(pd_rows)
            summaries.append(
                {
                    "url": url,
                    "status": "written",
                    "output_file": str(output_file),
                    "selected_files": len(selected),
                    "selected_lines": sum(int(item["lines"]) for item in selected),
                    "selected_words": sum(int(item["words"]) for item in selected),
                }
            )
            shutil.rmtree(repo_dir, ignore_errors=True)
    summary_path = output_dir / "summary.csv"
    fieldnames = sorted({key for row in summaries for key in row})
    with summary_path.open("w", newline="", encoding="utf-8-sig") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(summaries)
